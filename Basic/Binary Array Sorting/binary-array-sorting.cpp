class Solution{
  public:
    
    // A[]: input array
    // N: input array
    //Function to sort the binary array.
    void binSort(int A[], int N)
    {
       int low=0;
      int high=N-1;//mistake
      while(low<=high)
      {
          if(A[low]==0)
          {
              low++;
              
          }
          else if(A[high]==1)
          {
              high--;
              
          }
          else
          {
              int temp=A[low];
              A[low]=A[high];
              A[high]=temp;
              low++;
              high--;
          }
      }
    }
};

